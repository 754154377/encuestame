package org.encuestame.utils.web.notification;

import java.io.Serializable;

/**
 * Description Class.
 * @author Picado, Juan juanATencuestame.org
 * @since Sep 18, 2010 11:17:11 PM
 * @version Id:
 */
public class UtilNotification implements Serializable {

    /**
     * Serial.
     */
    private static final long serialVersionUID = 74374939382833094L;

    private String description;

    private Long id;

    private String date;

    private String type;

    private String icon;

    private String hour;

    private String additionalDescription;

    private String url;

    private Boolean readed;

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the hour
     */
    public String getHour() {
        return hour;
    }

    /**
     * @param hour the hour to set
     */
    public void setHour(String hour) {
        this.hour = hour;
    }

    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * @return the icon
     */
    public String getIcon() {
        return icon;
    }

    /**
     * @param icon the icon to set
     */
    public void setIcon(String icon) {
        this.icon = icon;
    }

    /**
     * @return the additionalDescription
     */
    public String getAdditionalDescription() {
        return additionalDescription;
    }

    /**
     * @param additionalDescription the additionalDescription to set
     */
    public void setAdditionalDescription(String additionalDescription) {
        this.additionalDescription = additionalDescription;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @param url the url to set
     */
    public void setUrl(final String url) {
        this.url = url;
    }

    /**
     * @return the readed
     */
    public Boolean getReaded() {
        return readed;
    }

    /**
     * @param readed the readed to set
     */
    public void setReaded(final Boolean readed) {
        this.readed = readed;
    }
}
